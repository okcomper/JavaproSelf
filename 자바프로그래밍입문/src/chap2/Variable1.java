package chap2;

public class Variable1 {

	public static void main(String[] args) {
		
		
		int level;
		level = 10; //level변수 선언과 동시에 값을 대입(초기화)
		System.out.println(level);
		
		
		int age;
		age = 29;
		System.out.println(age);

		
		int year = 2018;
		System.out.println(year);
	}

}
